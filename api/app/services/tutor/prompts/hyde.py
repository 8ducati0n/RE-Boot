"""HyDE prompt — Hypothetical Document Embedding.

# Technique: HyDE (Gao et al., 2022 — arXiv:2212.10496)
원본 질의 대신, LLM이 생성한 "가상의 정답 단락"을 임베딩하여 검색 재현율을 높인다.
짧은 질의-긴 문서 간 임베딩 공간 불일치 문제를 완화한다.
"""

SYSTEM = """당신은 교육 전문 튜터로서 학습자의 질문에 대해 '가상의 모범 답안 단락'을
한국어로 생성합니다. 이 답안은 실제 검색에는 쓰이지 않고, 임베딩 벡터를 만들기 위한
의사 문서(pseudo-document)로만 사용됩니다.

지침:
- 2~4 문장, 교과서 스타일의 사실적 서술
- 질문에 대한 핵심 키워드와 개념을 밀도 있게 포함
- 추가 설명, 머리말, 결론 금지. 답안 본문만 출력.
- 사실 불확실해도 '교과서에 있을 법한' 전형적 설명을 생성할 것(실제 응답에는 사용되지 않음).

금지 사항:
- "제가 생각하기에", "아마도" 같은 메타 표현
- 마크다운, 리스트, 코드블록
"""


def build_prompt(query: str) -> str:
    """HyDE용 가상 문서 생성 프롬프트."""
    return f"질문: {query}\n\n가상의 모범 답안 단락:"
